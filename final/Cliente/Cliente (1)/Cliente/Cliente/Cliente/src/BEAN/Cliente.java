
package BEAN;

public class Cliente {
    
    private int IdCliente;
    private String NombCliente;
    private String ApellCliente;
    private String DocCliente;
    private int Sexo;
    private String Nacionalidad;
    private String FechNacimiento;
    private String Direccion;
    private int TelfCliente;
    private String Correo;

    public Cliente() {
    }

    public int getIdCliente() {
        return IdCliente;
    }

    public void setIdCliente(int IdCliente) {
        this.IdCliente = IdCliente;
    }

    public String getNombCliente() {
        return NombCliente;
    }

    public void setNombCliente(String NombCliente) {
        this.NombCliente = NombCliente;
    }

    public String getApellCliente() {
        return ApellCliente;
    }

    public void setApellCliente(String ApellCliente) {
        this.ApellCliente = ApellCliente;
    }

    public String getDocCliente() {
        return DocCliente;
    }

    public void setDocCliente(String DocCliente) {
        this.DocCliente = DocCliente;
    }

    public int getSexo() {
        return Sexo;
    }

    public void setSexo(int Sexo) {
        this.Sexo = Sexo;
    }

    public String getNacionalidad() {
        return Nacionalidad;
    }

    public void setNacionalidad(String Nacionalidad) {
        this.Nacionalidad = Nacionalidad;
    }

    public String getFechNacimiento() {
        return FechNacimiento;
    }

    public void setFechNacimiento(String FechNacimiento) {
        this.FechNacimiento = FechNacimiento;
    }

    public String getDireccion() {
        return Direccion;
    }

    public void setDireccion(String Direccion) {
        this.Direccion = Direccion;
    }

    public int getTelfCliente() {
        return TelfCliente;
    }

    public void setTelfCliente(int TelfCliente) {
        this.TelfCliente = TelfCliente;
    }

    public String getCorreo() {
        return Correo;
    }

    public void setCorreo(String Correo) {
        this.Correo = Correo;
    }

    
    
            
}
