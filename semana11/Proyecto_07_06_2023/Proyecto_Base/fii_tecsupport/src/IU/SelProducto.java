    public void llenaTblProducto(boolean sw, String cad){
        Vector<Producto> listProducto = produDao.ListaItem(sw, cad);
        dtm.setRowCount(0);
        for(int i = 0; i<listProducto.size();i++){
            Vector vec = new Vector();
            vec.addElement(listProducto.get(i).getId_producto());
            vec.addElement(listProducto.get(i).getDescripcion());
            vec.addElement(listProducto.get(i).getTipo());
            vec.addElement(listProducto.get(i).getPrecio());
            vec.addElement(listProducto.get(i).getEstado());
            dtm.addRow(vec);
            
        }  
    }
    
